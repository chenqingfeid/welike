package com.pekingese.pagestack.framework.view;

import android.view.animation.Interpolator;

class PageStackInterpolator implements Interpolator {

    @Override
    public float getInterpolation(float t) {
        t -= 1.0f;
        return t * t * t * t * t + 1.0f;
    }
}
